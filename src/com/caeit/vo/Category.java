package com.caeit.vo;

public class Category {

	private int id;
	private int pid;
	private String name;

	public int getId() {
		return this.id;
	}

	public int getPid() {
		return this.pid;
	}

	public String getName() {
		return this.name;
	}

	public void setId(int id) {
		this.id = id;
	}

	public void setPid(int pid) {
		this.pid = pid;
	}

	public void setName(String name) {
		this.name = name;
	}

}
